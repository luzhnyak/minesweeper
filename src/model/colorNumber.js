export const colorNumber = number => {
  switch (number) {
    case 1:
      return 'blue';
    case 2:
      return 'green';
    case 3:
      return 'red';
    case 4:
      return 'darkblue';
    case 5:
      return 'darkgreen';
    case 6:
      return 'darkred';
    default:
      return 'black';
  }
};
